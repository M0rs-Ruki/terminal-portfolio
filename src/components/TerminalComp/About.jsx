// Typewriter effect component
const TypewriterText = ({ text, delay = 50 }) => {
  const [displayText, setDisplayText] = useState('');
  const [currentIndex, setCurrentIndex] = useState(0);

  useEffect(() => {
    if (currentIndex < text.length) {
      const timeout = setTimeout(() => {
        setDisplayText(prev => prev + text[currentIndex]);
        setCurrentIndex(prev => prev + 1);
      }, delay);

      return () => clearTimeout(timeout);
    }
  }, [currentIndex, text, delay]);

  return (
    <span>
      {displayText}
      {currentIndex < text.length && <span className="animate-pulse text-green-400">|</span>}
    </span>
  );
};

// Matrix rain effect (responsive)
const MatrixRain = () => {
  const chars = "01";
  // Responsive columns - fewer on mobile
  const columns = window.innerWidth < 768 ? 20 : 50;
  
  return (
    <div className="absolute inset-0 overflow-hidden pointer-events-none opacity-5">
      {Array.from({ length: columns }).map((_, i) => (
        <div
          key={i}
          className="absolute text-green-400 text-xs font-mono"
          style={{
            left: `${(i * 100) / columns}%`,
            animationDelay: `${Math.random() * 5}s`,
            animation: 'matrix-fall 10s linear infinite'
          }}
        >
          {Array.from({ length: 20 }).map((_, j) => (
            <div key={j} className="opacity-20">
              {chars[Math.floor(Math.random() * chars.length)]}
            </div>
          ))}
        </div>
      ))}
    </div>
  );
};

